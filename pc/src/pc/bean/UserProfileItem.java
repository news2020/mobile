package pc.bean;

public class UserProfileItem {

	private String itemLableName;
	private String itemValue;
	private String userProfileType;
	public String getItemLableName() {
		return itemLableName;
	}
	public void setItemLableName(String itemLableName) {
		this.itemLableName = itemLableName;
	}
	public String getItemValue() {
		return itemValue;
	}
	public void setItemValue(String itemValue) {
		this.itemValue = itemValue;
	}
	public String getUserProfileType() {
		return userProfileType;
	}
	public void setUserProfileType(String userProfileType) {
		this.userProfileType = userProfileType;
	}
}
